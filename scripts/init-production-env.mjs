if (fs.existsSync(target)) {
  const existingPairs = parsePairs(fs.readFileSync(target, "utf8"));
  const existing = new Map(existingPairs);
  const changes = [];
  for (const [key, defaultVal] of replacements) {
    if (!existing.has(key)) {
      changes.push(`  added missing ${key}`);
      existing.set(key, defaultVal !== null ? String(defaultVal) : secretFor(key));
    } else if (needsReplace(key) && isPlaceholder(existing.get(key))) {
      changes.push(`  regenerated placeholder ${key}`);
      existing.set(key, secretFor(key));
    }
  }
  // Rebuild from example to preserve ordering, keeping existing values
  const exampleLines = fs.readFileSync(example, "utf8").split(/\r?\n/);
  const out = [];
  let pendingDbPass = "";
  for (const line of exampleLines) {
    if (!line || line.trim().startsWith("#") || !line.includes("=")) { out.push(line); continue; }
    const idx = line.indexOf("=");
    const key = line.slice(0, idx);
    if (key === "MYSQL_PASSWORD") {
      pendingDbPass = existing.get("MYSQL_PASSWORD") ?? replacements.get("MYSQL_PASSWORD") ?? secret(24);
      out.push(`MYSQL_PASSWORD=${pendingDbPass}`);
    } else if (key === "DB_PASSWORD") {
      out.push(`DB_PASSWORD=${pendingDbPass || existing.get("DB_PASSWORD") || secret(24)}`);
    } else if (key === "BUILD_COMMIT") {
      const ev = existing.get("BUILD_COMMIT");
      out.push(`BUILD_COMMIT=${ev && !isPlaceholder(ev) ? ev : "replace-with-release-commit-or-image-digest"}`);
    } else if (existing.has(key)) {
      out.push(`${key}=${existing.get(key)}`);
    } else {
      out.push(`${key}=${line.slice(idx + 1)}`);
    }
  }
  out.unshift(
    "# Generated by npm run init:production-env.",
    "# Replace all example domains, BUILD_COMMIT, SMS provider credentials, and optional provider credentials before running npm run preflight.",
    "# SMS provider credentials are preferably maintained in the admin System Settings page.",
    ""
  );
  fs.writeFileSync(target, `${out.join("\n").replace(/\n*$/, "\n")}`, "utf8");
  if (changes.length) {
    console.log("Patched deploy/.env.production:");
    for (const c of changes) console.log(c);
  } else {
    console.log("deploy/.env.production is up-to-date; no changes needed.");
  }
  process.exit(0);
}

// Fresh generation
const lines = fs.readFileSync(example, "utf8").split(/\r?\n/);
let mysqlPassword = "";
const output = lines.map((line) => {
  if (!line || line.trim().startsWith("#") || !line.includes("=")) return line;
  const index = line.indexOf("=");
  const key = line.slice(0, index);
  if (key === "MYSQL_PASSWORD") mysqlPassword = replacements.get("MYSQL_PASSWORD") || "";
  if (key === "DB_PASSWORD") return `DB_PASSWORD=${mysqlPassword || secret(24)}`;
  if (key === "BUILD_COMMIT") return "BUILD_COMMIT=replace-with-release-commit-or-image-digest";
  if (replacements.has(key)) return `${key}=${replacements.get(key)}`;
  return line;
});

output.unshift(
  "# Generated by npm run init:production-env.",
  "# Replace all example domains, BUILD_COMMIT, SMS provider credentials, and optional provider credentials before running npm run preflight.",
  "# SMS provider credentials are preferably maintained in the admin System Settings page.",
  ""
);

fs.writeFileSync(target, `${output.join("\n").replace(/\n*$/, "\n")}`, "utf8");
console.log("Created deploy/.env.production with fresh local secrets.");
console.log("Next: replace domains, BUILD_COMMIT, configure SMS provider credentials in the admin System Settings page, and replace any optional provider credentials before release.");
